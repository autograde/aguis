package models_test

import (
	"testing"

	"github.com/autograde/aguis/models"
)

func TestIsCourseRepo(t *testing.T) {
	x := models.UserRepo
	if x.IsCourseRepo() {
		t.Fatal("expected IsCourseRepo to return false for UserRepo")
	}
	x = models.AssignmentsRepo
	if !x.IsCourseRepo() {
		t.Fatal("expected IsCourseRepo to return true for AssignmentsRepo")
	}
	x = models.CourseInfoRepo
	if !x.IsCourseRepo() {
		t.Fatal("expected IsCourseRepo to return true for CourseInfoRepo")
	}
	x = models.SolutionsRepo
	if !x.IsCourseRepo() {
		t.Fatal("expected IsCourseRepo to return true for SolutionsRepo")
	}
	x = models.TestsRepo
	if !x.IsCourseRepo() {
		t.Fatal("expected IsCourseRepo to return true for TestsRepo")
	}
}
